    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
       try{
            String nombre = validarNombre();
            String paterno = validarPaterno();
            String materno = validarMaterno();
            double cal  = validarCalificacion();
                    
            
            
                Alumno alumno = new Alumno(
                edtNombre.getText(),
                edtPaterno.getText(),
                edtMaterno.getText(),
                Double.valueOf(edtCalificacion.getText()),
                (Carreras)cmbCarrera.getSelectedItem()
        );
        modalResult = ModalResult.mrAceptar;
       this.setVisible(false);
       }catch(CalificacionException e){
           JOptionPane.showMessageDialog(this, e.getMessage(),"¡Error!", JOptionPane.ERROR_MESSAGE);
           edtCalificacion.requestFocus();
       }catch(PaternoException e){
           JOptionPane.showMessageDialog(this, e.getMessage(), "¡Error!", JOptionPane.ERROR_MESSAGE);
           edtPaterno.requestFocus();;
       } catch(MaternoException e){
           JOptionPane.showMessageDialog(this, e.getMessage(), "¡Error!", JOptionPane.ERROR_MESSAGE);
           edtMaterno.requestFocus();
       } catch(NombreException e){
           JOptionPane.showMessageDialog(this, e.getMessage(), "¡Error!", JOptionPane.ERROR_MESSAGE);
           edtNombre.requestFocus();
       }
    }//GEN-LAST:event_btnAceptarActionPerformed
    
