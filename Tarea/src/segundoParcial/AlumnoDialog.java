   private double validarCalificacion()throws CalificacionException{
       double cal = -1;
       
       try{
           cal = Double.valueOf(edtCalificacion.getText());
           if((cal<0.0)|| (cal>10.0)){
               
               throw new CalificacionException();
           }
           return cal;
       }catch(Exception e){
           
           throw new CalificacionException("Hubo un error en la calificación");
       } 
   }
   
    private String validarNombre() throws NombreException{
        if("".equals(edtNombre.getText())){
            throw new NombreException("¡Nombre Vacio!");            
        }   
        return "si";          
       }
    
     private String validarPaterno() throws PaternoException{
        if("".equals(edtPaterno.getText())){
            throw new PaternoException("¡Apellido Paterno vacio!");            
        }   
        return "si";          
       }
      private String validarMaterno() throws MaternoException{
        if("".equals(edtMaterno.getText())){
            throw new MaternoException("¡Apellido materno vacio!");            
        }   
        return "si";          
       }
      

    

