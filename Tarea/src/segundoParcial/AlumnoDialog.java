    public ModalResult mostrarDialogo(){
        modalResult = ModalResult.mrCancelar;
        edtNombre.setText("");
        edtPaterno.setText("");
        edtMaterno.setText("");
        edtCalificacion.setText("");
        cmbCarrera.setSelectedIndex(0);
        this.setVisible(true);
        return modalResult;
    }
    
    
    
    public ModalResult mostrarAlumno(ArrayList<Alumno> alumnos, int n){
         modalResult = ModalResult.mrCancelar;
        edtNombre.requestFocus();
        edtNombre.setText(alumnos.get(n).getNombre());
        edtPaterno.setText(alumnos.get(n).getPaterno());
        edtMaterno.setText(alumnos.get(n).getMaterno());
        edtCalificacion.setText(alumnos.get(n).getCalificacion().toString());
        cmbCarrera.setSelectedIndex(0);
        this.setVisible(true);
        return modalResult;

        
    }
    
    public Alumno getAlumno(){
        Alumno alumno = new Alumno(
                edtNombre.getText(),
                edtPaterno.getText(),
                edtMaterno.getText(),
                Double.valueOf(edtCalificacion.getText()),
                (Carreras)cmbCarrera.getSelectedItem()
        );
        
        return alumno;
        
    }
    
    
