    private void menuModiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuModiActionPerformed
        int n = tblAlumnos.getSelectedRow();
        if(n<0){
            JOptionPane.showMessageDialog(this,"No has seleccionado nada", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            
            ModalResult mr = dlgAlumno.mostrarAlumno(alumnos, n);
            
            if(mr == ModalResult.mrAceptar){
                
                Alumno alumno = dlgAlumno.getAlumno();
                alumnos.set(n, alumno);
                alumnosModel.fireTableDataChanged();
             }
         
        }
    }//GEN-LAST:event_menuModiActionPerformed

